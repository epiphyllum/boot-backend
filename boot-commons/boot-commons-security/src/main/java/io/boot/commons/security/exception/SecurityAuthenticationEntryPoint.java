package io.boot.commons.security.exception;

import io.boot.commons.tools.exception.ErrorCode;
import io.boot.commons.tools.utils.JsonUtils;
import io.boot.commons.tools.utils.Result;
import jakarta.servlet.ServletException;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import org.springframework.security.core.AuthenticationException;
import org.springframework.security.web.AuthenticationEntryPoint;

import java.io.IOException;

/**
 * 匿名用户(token不存在、错误)，异常处理器
 *
 * @author epiphyllum.zhou@gmail.com
 */
public class SecurityAuthenticationEntryPoint implements AuthenticationEntryPoint {

    @Override
    public void commence(HttpServletRequest request, HttpServletResponse response, AuthenticationException authException) throws IOException, ServletException {
        response.setContentType("application/json;charset=UTF-8");
        response.getWriter().print(JsonUtils.toJsonString(new Result<>().error(ErrorCode.UNAUTHORIZED)));
    }
}